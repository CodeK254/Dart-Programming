void main(){
  // For Loop.
  print("\n\nFor Loop:\n\n");
  int i;
  for(i = 0; i < 10; i++){
    print("\ni is less than 10 when it is $i");
  }
  // While Loop.
  print("\n\nWhile Loop:\n\n");
  int j = 0;
  while(j < 10){
    print("\nj is less than 10 when it is $j");
    j++;
  }
  // Do...While Loop.
  print("\n\nDo While Loop:\n\n");
  int k = 0;
  do{
    print("\nk is less than 10 when it is $k");
    k++;
  } while(k < 10);
}
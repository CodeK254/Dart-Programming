void main(){
  //Declaring 4 double variables: x, y, z, avg;
  double x, y, z, avg;

  //Assigning x, y, z as we want;
  x = 15;
  y = 20;
  z = 25;

  //Calculating the average of x, y, z, and assigning it to avg;
  avg = (x + y + z) / 3; 

  //Printing the average
  print("\nThe average of $x, $y, $z, is $avg\n");
}